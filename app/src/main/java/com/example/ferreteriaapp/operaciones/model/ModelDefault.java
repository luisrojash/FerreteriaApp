package com.example.ferreteriaapp.operaciones.model;

public class ModelDefault {

    private String idModel;
    private String nombreModel;
    private String idAlamacen;

    public String getIdModel() {
        return idModel;
    }

    public void setIdModel(String idModel) {
        this.idModel = idModel;
    }

    public String getNombreModel() {
        return nombreModel;
    }

    public void setNombreModel(String nombreModel) {
        this.nombreModel = nombreModel;
    }

    public String getIdAlamacen() {
        return idAlamacen;
    }

    public void setIdAlamacen(String idAlamacen) {
        this.idAlamacen = idAlamacen;
    }
}
